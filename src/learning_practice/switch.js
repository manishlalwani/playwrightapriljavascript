let browser = 'safari';

switch (browser) {
    case 'chrome':
        console.log("Launch Chrome");
        break;
    case 'firefox':
        console.log("Launch firefox");
        break;
    case 'edge':
        console.log("Launch edge");
        break;

    case 'safari':
        console.log("Launch safari");
        break;
    default:
        console.log("Invalid Browser.........");
        break;
}